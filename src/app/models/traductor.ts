export class Traductor {
    traduccionIn: string;
    traduccionOut: string;
    idiomaIn: string;
    idiomaOut: string;

    constructor(textoIn?: string, textoOut?:string, idiomaIn?:string, idiomaOut?:string){
        this.traduccionIn = this.traduccionIn;
        this.traduccionOut = this.traduccionOut;
        this.idiomaIn = idiomaIn;
        this.idiomaOut = idiomaOut;
    }
}
